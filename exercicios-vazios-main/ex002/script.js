// Pede ao usuário que digite o nome
// prompt() abre uma caixinha para digitar
// O valor digitado é guardado na variável "nome"
let nome = prompt ("Qual é o seu nome?");

// Pede ao usuário que digite a cor favorita
// O valor digitado é guardado na variável "corFavorita"
let corFavorita = prompt ("Qual é a sua cor favorita?");

// Mostra uma mensagem na tela usando alert()
// Concatenamos a mensagem com as variáveis "nome" e "corFavorita"
alert ("Meu nome é " + nome + ", E minha cor favorita é " + corFavorita + "!");