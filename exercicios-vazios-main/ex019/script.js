// 1. Pergunta ao usuário a base do triângulo
let base = prompt("Digite a base do triângulo: ");

// 2. Pergunta ao usuário a altura do triângulo
let altura = prompt("Digite a altura do triângulo: ");

// 3. Converte os valores digitados de texto para número
base = Number (base);
altura - Number (altura);

// 4. Calcula a área do triângulo
let area = (base * altura) / 2;

// 5. Mostra a área para o usuário
alert("A área do triangulo é: " + area);
