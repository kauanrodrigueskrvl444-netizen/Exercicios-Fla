// Pede ao usuário que digite um número
// prompt() abre uma caixinha para digitar
// Number() converte o texto digitado em número
let numero = Number(prompt("Digite um numero: "));

// Calcula a metade do número
// Dividimos o número por 2
let metade = numero / 2;

// Mostra o resultado na tela usando alert()
// Concatenamos a mensagem com o valor armazenado em "metade"
alert (" A metade de " + numero + " é " + metade);