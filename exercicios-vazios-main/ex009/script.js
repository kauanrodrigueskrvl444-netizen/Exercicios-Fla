// cria uma variável chamada "numero"
// prompt mostra uma caixa pedindo um valor ao usuário
// Number converte o valor digitado (texto) em número
let numero = Number (prompt("Digite um numero: "));

// cria uma variável chamada "resultado"
// divide o número digitado por 2 para calcular a metade
let resultado = numero / 2;

// mostra uma mensagem na tela com o resultado da conta
alert("A metade do numero é: " + resultado)