// Criamos uma variável chamada "valor1"
// prompt() abre uma caixinha pedindo ao usuário o primeiro número
// Number() transforma o que o usuário digitou (texto) em númer
let valor1 = Number (prompt("Digite o primeiro valor: "));


// Criamos uma variável chamada "valor2"
// prompt() pede o segundo número ao usuário
// Number() converte o texto em número
let valor2 = Number (prompt("Digite o segundo valor: "));

// Criamos uma variável chamada "resultado"
// Usamos o operador "*" para multiplicar valor1 pelo valor2
let resultado = valor1 * valor2;


// Mostramos o resultado na tela usando alert()
// Concatenamos a mensagem com o valor armazenado em "resultado"
alert (" O resultado da multiplicação é: " + resultado)